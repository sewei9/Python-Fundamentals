# Positive Zahl
x = 12/7
print()
print("x:", x)

# RUndung und Konvertierung
# Mithilfe der eingebauten FUnktion round() wird das Ergebnis auf drei Stellen nach dem Komma gerundet.
rx = round(x, 3)
print("x gerundet auf drei Stellen: ", rx)
# Mit der gleichen Funktion wird das Ergebnis auf die nächsthöhere oder nächstniedrige ganze Zahl gerundet.
rx = round(x)
print("x gerundet auf null Stellen: ", rx)
# Mithilfe der Funktion int() wird das Ergebnis in ein ganze Zahl umgewandelt. DIe Stellen nach dem Komma werden abgeschnitten.
ix = int(x)
print("int(x):", ix)
print()

